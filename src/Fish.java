public class Fish {
   private String name;
   private String ecologicalGroup;
   private String classification;
   private String size;
   private String predatoryOrNonPredatory;
   private String wildOrPets;

   public String getName() {
      return name;
   }

   public void setName(String name) {
      this.name = name;
   }

   public String getEcologicalGroup() {
      return ecologicalGroup;
   }

   public void setEcologicalGroup(String ecologicalGroup) {
      this.ecologicalGroup = ecologicalGroup;
   }

   public String getClassification() {
      return classification;
   }

   public void setClassification(String classification) {
      this.classification = classification;
   }

   public String getSize() {
      return size;
   }

   public void setSize(String size) {
      this.size = size;
   }

   public String getPredatoryOrNonPredatory() {
      return predatoryOrNonPredatory;
   }

   public void setPredatoryOrNonPredatory(String predatoryOrNonPredatory) {
      this.predatoryOrNonPredatory = predatoryOrNonPredatory;
   }

   public String getWildOrPets() {
      return wildOrPets;
   }

   public void setWildOrPets(String wildOrPets) {
      this.wildOrPets = wildOrPets;
   }
}
